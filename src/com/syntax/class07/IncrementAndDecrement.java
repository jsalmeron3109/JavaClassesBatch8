package com.syntax.class07;

public class IncrementAndDecrement {

	public static void main(String[] args) {
		int num=90;
		num+=1;//(num=num+1; faster way)
		num++; //increment operator (adds 1 to variable) 
		num--;//decrement operator(subtracts variable by 1)
		
		System.out.println(num);
		
		//increment and decrement operators can be used ONLY with variables
		//will only add or subtract 1 (no other value)
		
		//Loops -> help execute same code and avoid repetitive code
		//types of loop: while loop, do while loop, for lopp, enhanced loop
		
	
	
	
}
}